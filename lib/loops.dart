
void main(){

  for(int i=1; i<10; i++){      //As the term loop explained itself that repeatation of something.
    print(i);                    //for-loops are count specific, it means, we'will print anything specific to counting.
  }                               //for example => if i wanna print numbers from 1 to 10 so this is count specific.


  int i=10;
  while(i<=20){     //While Loops are condition based loops.
    print(i);
    i++;
  }

  int a=1;
  do{
    print("Hello");     //This is do-while loop. In this loop no matter if the condition is false but it always executes once.
    a++;
  }while(a<=10);
}
